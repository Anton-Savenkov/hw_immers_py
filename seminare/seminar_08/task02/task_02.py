import json
import os

def update_user_data():
    user_data = {}
    file_json = 'user_data.json'

    if os.path.exists(file_json):  # –ï—Å–ª–∏ —Ñ–∞–π–ª —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –∑–∞–≥—Ä—É–∂–∞–µ–º –¥–∞–Ω–Ω—ã–µ
        with open(file_json, 'r') as file:
            user_data = json.load(file)

    while True:
        name = input('–í–≤–µ–¥–∏—Ç–µ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–∏–ª–∏ "exit" –¥–ª—è –≤—ã—Ö–æ–¥–∞): ')
